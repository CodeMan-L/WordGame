/**
 * 从数组中随机获取一个元素
 * @param {Array} array - 源数组
 * @returns {*} 随机元素
 */
function getRandomItem(array) {
  if (!Array.isArray(array) || array.length === 0) {
    return null;
  }
  return array[Math.floor(Math.random() * array.length)];
}

/**
 * 根据纪元生成一个事件，附带描述和NPC反应
 * @param {string} era - 纪元标识符
 * @returns {Object|null} 包含 event, description, npc_reaction 的事件对象
 */
function generateEvent(era) {
  var pool = TEXT_POOL[era];
  if (!pool || !pool.events || pool.events.length === 0) {
    return null;
  }

  var event = getRandomItem(pool.events);
  var description = getRandomItem(pool.descriptions);
  var npc_reaction = getRandomItem(pool.npc_reactions);

  if (!event) {
    return null;
  }

  // 扁平化合并：事件属性 + effects展开 + 描述和NPC反应
  var result = Object.assign({}, event);
  if (event.effects) {
    Object.assign(result, event.effects);
  }
  result.description = description;
  result.npc_reaction = npc_reaction;

  return result;
}

/**
 * 获取联邦年BOSS选项
 * @returns {Array|null} BOSS选项数组
 */
function getBossOptions() {
  var federation = TEXT_POOL.federation;
  if (!federation || !federation.boss_options) {
    return null;
  }
  return federation.boss_options;
}
